class Product {
  final String id;
  final String name;
  final String code;
  final String photo;
  final String unitPrice;
  final String quantity;
  final String totalPrice;
  final String createdAt;

  Product({
    required this.id,
    required this.name,
    required this.code,
    required this.photo,
    required this.unitPrice,
    required this.quantity,
    required this.totalPrice,
    required this.createdAt,
  });
}
