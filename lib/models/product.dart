class Product {
  final String id;
  final String name;
  final String code;
  final String photo;
  final double unitPrice;
  final int quantity;
  final double totalPrice;

  Product({
    required this.id,
    required this.name,
    required this.code,
    required this.photo,
    required this.unitPrice,
    required this.quantity,
    required this.totalPrice,
  });
}
